  void _validarLogin(context) async{

    var _pUsuario = _controleUsuario.text;
    var _pSenha =  _controleSenha.text; // Todo criptografar

    String url = "https://online.br.sgs.com/sementesapi/api/tb_petrovinas_usuario/?pUsuario=$_pUsuario&pSenha=$_pSenha";

    print("retorno: " + url);

    http.Response response = await http.get(url);
    Map<String, dynamic> retorno = json.decode(response.body);

    if (retorno != null){

      setState(() {
        if (_pUsuario == retorno["usuario"].toString() ){
          print("retorno = Usuário autenticado");

          Navigator.pushNamed(context, "/scanner");
        } else {
          print("Erro No Login");
        }
      });

    }


  }

